	FOR rec in (SELECT UNIQUE record_id FROM record_track)
    LOOP
    	UPDATE record
   		SET duration = (SELECT sum(duration) FROM track t, record_track rt WHERE t.track_id = rt.track_id and rt.record_id= rec.record_id)
   		WHERE record_id = rec.record_id;
    END LOOP;      
    COMMIT;
END;
