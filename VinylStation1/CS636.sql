BEGIN
	FOR rec in (SELECT UNIQUE invoice_id FROM record)
    LOOP
    	UPDATE invoice
   		SET total_price = (SELECT sum(price) FROM record WHERE invoice_id= rec.invoice_id)
   		WHERE invoice_id = rec.invoice_id;
    END LOOP;      
    COMMIT;
END;
